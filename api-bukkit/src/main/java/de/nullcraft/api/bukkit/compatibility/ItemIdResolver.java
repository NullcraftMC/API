    public static Material getMaterialById(int id) {
        return IDS.get(id);
    }

    public static int getIdByMaterial(Material material) {
        Integer id = IDS.inverse().get(material);

        if (id == null)
            throw new RuntimeException("Unmapped material: " + material.name());

        return id;
    }

